package biblioteca;

public class Edicao {
	private Long id;
	private String descricao;
	public Long getId() {
		return id;
	}
	
	// Getters and Setters
	
	public void setId(Long id) {
		this.id = id;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	

}
