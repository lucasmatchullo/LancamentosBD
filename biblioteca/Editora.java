package biblioteca;

public class Editora {
	private Long id;
	private String nome;
	public Long getId() {
		return id;
	}
	
	// Getters and Setters
	
	public void setId(Long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	

}